export function fmtPrice(v: number | undefined, unit: "eurM" | "eurM2" | "M" = "eurM2") {
  if (v === undefined || v === null) return "—";
  if (unit === "eurM2") return `€${v.toLocaleString("en-US")}/m²`;
  if (unit === "eurM") return `€${v.toLocaleString("en-US")}`;
  return `€${v.toFixed(1)}M`;
}
